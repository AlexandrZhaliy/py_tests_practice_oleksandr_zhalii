my_name = "Olek6sandr Zhalii"


print(my_name.upper()) #all to uppercase
print(my_name.lower()) #all to lowercase
print(my_name.title()) # first word letters to uppercase (after any text-divider)
print(my_name.capitalize()) # first sentence letter to uppercase (if it's letter)

print(my_name.islower())
print(my_name.isupper())
print(my_name.istitle())